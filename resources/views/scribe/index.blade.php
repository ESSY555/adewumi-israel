                                <h2 id="secure-drop-api-POSTapi-v1-secrets">Create a Secret</h2>

<p>
</p>

<p>Creates a new secure note that will be permanently deleted after being viewed once.</p>

<span id="example-requests-POSTapi-v1-secrets">
<blockquote>Example request:</blockquote>


<div class="bash-example">
    <pre><code class="language-bash">curl --request POST \
    "http://localhost/api/v1/secrets" \
    --header "Content-Type: application/json" \
    --header "Accept: application/json" \
    --data "{
    \"text\": \"\\\"My secret password\\\"\",
    \"ttl\": 60
}"
</code></pre></div>


<div class="javascript-example">
    <pre><code class="language-javascript">const url = new URL(
    "http://localhost/api/v1/secrets"
);

const headers = {
    "Content-Type": "application/json",
    "Accept": "application/json",
};

let body = {
    "text": "\"My secret password\"",
    "ttl": 60
};

fetch(url, {
    method: "POST",
    headers,
    body: JSON.stringify(body),
}).then(response =&gt; response.json());</code></pre></div>

